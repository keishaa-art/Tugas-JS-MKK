// //Menentukan angka per hari
let n = Number(prompt("Angka Dalam Hari"));

if (n == 1) {
    alert ('Senin');
  } else if (n == 2) {
    alert ('Selasa');
  } else if (n == 3) {
    alert ('Rabu');
  } else if (n == 4) {
    alert ('Kamis');
  } else if (n == 5) {
    alert ('Jumat');
  } else if (n == 6) {
    alert ('Sabtu');
  } else if (n == 7) {
    alert ('Minggu');
  } else {
    alert ('Tidak ada lagi');
  }
